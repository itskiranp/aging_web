            <p style="font-size:1rem; margin-top:5px; text-align:justify;">
                The training focused on the Nepal Harmonized Cognitive Assessment Protocol (N-HCAP) to strengthen enumerators’ skills in cognitive assessments, covering memory, attention, language, executive function, visuospatial skills, and processing speed. Participants engaged in demonstrations, certification, and field scenarios, with sessions on interview techniques, fieldwork challenges, and N-HCAP administration. Assessments included the Nepal Cognitive Screening Test (NCST), TICS, word recall, digit span, Symbol Bean Cancellation, word list recognition, logical memory, and advanced tasks such as constructional praxis, fluency, executive function, judgment, and the CSI-D assessment. 
